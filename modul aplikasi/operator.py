import math


def tambah(bil1,bil2):
    hitung = bil1+bil2
    print(f'hasil penjumlahan adalah: {hitung}')

def kurang(bil1,bil2):
    hasil = bil1-bil2
    print(f'hasil pengurangan adalah: {hasil}')

def kali(bil1,bil2):
    hasil = bil1*bil2
    print(f'hasil perkalian adalah: {hasil}')

def bagi(bil1,bil2):
    hasil = bil1/bil2
    print(f'hasil pembagian adalah: {hasil}')

def pangkat(bil1, bil2):
    hasil = bil1 ** bil2
    print (f'hasil pemangkatan adalah: {hasil}')

